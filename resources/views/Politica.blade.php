                    <h2>Informações Coletadas</h2>
                    <p>Ao utilizar os serviços da Loja, podemos coletar informações pessoais que você nos fornece voluntariamente, como nome, endereço de e-mail, endereço físico, número de telefone e informações de pagamento ao fazer uma compra.</p>
